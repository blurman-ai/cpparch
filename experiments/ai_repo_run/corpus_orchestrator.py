#!/usr/bin/env python3
"""Corpus-level orchestrator: run graph-drift + duplication + AI-detection on all C++ repos.

Reads all_local.tsv → filters C++ repos → parallel scan → aggregates corpus_report.md
"""

from __future__ import annotations

import argparse
import csv
import json
import subprocess
import sys
from concurrent.futures import ProcessPoolExecutor, as_completed
from datetime import datetime
from pathlib import Path


CPP_EXTS = {".h", ".hh", ".hpp", ".hxx", ".ipp", ".inc", ".c", ".cc", ".cpp", ".cxx", ".mm"}
AI_MARKERS = (
    "co-authored-by: claude",
    "co-authored-by: cursor",
    "co-authored-by: codex",
    "co-authored-by: github copilot",
    "co-authored-by: copilot",
    "noreply@anthropic.com",
    "cursor agent",
    "claude code",
    "generated by ai",
    "ai-assisted",
    "codex-",
    "cursor/",
    "cursor-",
    "copilot",
)

CORPUS_ROOT = Path("/home/localadm/oss")
CORPUS_SUBDIRS = ["_aidev_dense", "_aidev_run", "_aidev_lowstar", ""]  # Empty string for top-level
ARCHCHECK_BIN = Path("/home/localadm/projects/cpparch/build/debug/src/archcheck")
ORCHESTRATOR_DIR = Path("/home/localadm/projects/cpparch/experiments/ai_repo_run")
ALL_LOCAL_TSV = ORCHESTRATOR_DIR / "all_local.tsv"


def find_repo(name: str) -> Path | None:
    """Find repo directory by name, searching in CORPUS_SUBDIRS."""
    for subdir in CORPUS_SUBDIRS:
        if subdir:
            candidate = CORPUS_ROOT / subdir / name
        else:
            candidate = CORPUS_ROOT / name
        if candidate.is_dir() and (candidate / ".git").is_dir():
            return candidate
    return None


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Corpus-level orchestrator for graph+dup+AI scan")
    parser.add_argument(
        "--since",
        default="2025-05-01",
        help="only scan commits since this date (ISO 8601)",
    )
    parser.add_argument(
        "--cpp-min",
        type=int,
        default=5,
        help="minimum C/C++ files to include repo in scan",
    )
    parser.add_argument(
        "--parallel",
        type=int,
        default=4,
        help="number of parallel worker processes",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=0,
        help="max repos to scan (0 = all)",
    )
    parser.add_argument(
        "--skip-graph",
        action="store_true",
        help="skip expensive graph-drift scan (collect only AI/dup metrics)",
    )
    parser.add_argument(
        "--skip-dup",
        action="store_true",
        help="skip duplication scan",
    )
    return parser.parse_args()


def git(repo: Path, *args: str, timeout: int = 60, check: bool = False) -> subprocess.CompletedProcess[str]:
    """Run git command in repo."""
    return subprocess.run(
        ["git", "-C", str(repo), *args],
        capture_output=True,
        text=True,
        timeout=timeout,
        check=check,
    )


def count_cpp_files(repo: Path) -> int:
    """Count C/C++ files in repo (excluding vendor/)."""
    count = 0
    for ext in CPP_EXTS:
        # Use find, exclude vendor paths
        result = subprocess.run(
            ["find", str(repo), "-name", f"*{ext}", "-not", "-path", "*/vendor/*", "-type", "f"],
            capture_output=True,
            text=True,
            timeout=30,
        )
        count += len(result.stdout.strip().splitlines())
    return count


def count_commits_since(repo: Path, since: str) -> int:
    """Count first-parent commits since date."""
    result = git(repo, "rev-list", "--count", "--first-parent", f"--since={since}", "HEAD")
    if result.returncode == 0:
        try:
            return int(result.stdout.strip())
        except ValueError:
            return 0
    return 0


def count_cpp_loc(repo: Path) -> int:
    """Count lines of C/C++ code (excluding vendor/)."""
    result = subprocess.run(
        ["find", str(repo), "!", "-path", "*/vendor/*", "-type", "f", "-name", "*.[hc]*"],
        capture_output=True,
        text=True,
        timeout=30,
    )
    if result.returncode != 0:
        return 0

    files = result.stdout.strip().splitlines()
    if not files:
        return 0

    loc = 0
    for file_path in files:
        try:
            with open(file_path, "r", encoding="utf-8", errors="ignore") as f:
                loc += sum(1 for _ in f if _.strip())
        except (OSError, IOError):
            pass
    return loc


def count_ai_commits(repo: Path, since: str) -> tuple[int, int]:
    """Count AI-marked commits in [since..HEAD]. Returns (ai_count, total_count)."""
    result = git(
        repo,
        "log",
        "--reverse",
        "--first-parent",
        f"--since={since}",
        "--format=%B%n---END---",
        "HEAD",
        timeout=120,
    )
    if result.returncode != 0:
        return 0, 0

    commits = result.stdout.split("---END---")
    ai_count = 0
    total = len([c for c in commits if c.strip()])

    for commit_text in commits:
        if not commit_text.strip():
            continue
        lowered = commit_text.lower()
        if any(marker in lowered for marker in AI_MARKERS):
            ai_count += 1

    return ai_count, total


def readme_text(repo: Path) -> str:
    """Get first non-empty line from README."""
    for readme in ["README.md", "README.rst", "README.txt", "README"]:
        readme_path = repo / readme
        if readme_path.exists():
            try:
                with open(readme_path, "r", encoding="utf-8", errors="ignore") as f:
                    for line in f:
                        line = line.strip()
                        if line and not line.startswith("#"):
                            return line[:80]
            except (OSError, IOError):
                pass
    return "(no README)"


def run_graph_drift(repo: Path, limit: int) -> int:
    """Run graph-drift analysis. Returns count of commits with graph errors."""
    script = ORCHESTRATOR_DIR / "generate_per_commit_graph_drift.py"
    if not script.exists():
        return 0

    try:
        result = subprocess.run(
            [sys.executable, str(script), str(repo), f"--limit={limit}"],
            capture_output=True,
            text=True,
            timeout=600,
        )
    except subprocess.TimeoutExpired:
        return 0
    except Exception:
        return 0

    if result.returncode not in (0, 1):  # 0=ok, 1=violations found
        return 0

    # Count non-zero records in output .jsonl
    jsonl_path = ORCHESTRATOR_DIR / f"{repo.name}_graph_drift.jsonl"
    if not jsonl_path.exists():
        return 0

    count = 0
    try:
        with open(jsonl_path, "r", encoding="utf-8") as f:
            for line in f:
                if not line.strip():
                    continue
                try:
                    record = json.loads(line)
                    # Count as error if any drift signal > 0
                    if any(
                        int(record.get(key, 0)) > 0
                        for key in ("added_edges", "removed_edges", "grown_cycles", "new_area_deps")
                    ):
                        count += 1
                except (json.JSONDecodeError, ValueError):
                    pass
    except (OSError, IOError):
        pass

    return count


def run_duplication(repo: Path) -> int:
    """Run duplication analysis on HEAD. Returns count of reported pairs."""
    if not ARCHCHECK_BIN.exists():
        return 0

    try:
        result = subprocess.run(
            [str(ARCHCHECK_BIN), "--duplication", str(repo)],
            cwd=repo,
            capture_output=True,
            text=True,
            timeout=600,
        )
    except subprocess.TimeoutExpired:
        return 0
    except Exception:
        return 0

    # Parse output to count reported pairs
    # Format: "reported 41 pairs above threshold"
    for line in result.stdout.splitlines():
        line_stripped = line.strip()
        if line_stripped.startswith("reported "):
            # Extract number after "reported"
            parts = line_stripped.split()
            if len(parts) > 1:
                try:
                    return int(parts[1])
                except ValueError:
                    pass

    return 0


def scan_repo(
    repo_name: str, since: str, cpp_min: int, skip_graph: bool = False, skip_dup: bool = False
) -> dict[str, object] | None:
    """Scan one repository. Returns record or None if skipped."""
    repo = find_repo(repo_name)
    if not repo:
        return None

    # Filter by C/C++ file count
    cpp_count = count_cpp_files(repo)
    if cpp_count < cpp_min:
        return None

    # Count commits and AI markers
    commit_count = count_commits_since(repo, since)
    if commit_count == 0:
        return None  # No commits in window

    ai_count, _ = count_ai_commits(repo, since)
    ai_pct = 100 * ai_count / commit_count if commit_count > 0 else 0

    # Count LOC
    cpp_loc = count_cpp_loc(repo)

    # Get README
    about = readme_text(repo)

    # Graph drift scan (optional, expensive)
    graph_errors = 0
    if not skip_graph:
        graph_errors = run_graph_drift(repo, commit_count)

    # Duplication scan (optional)
    dup_pairs = 0
    if not skip_dup:
        dup_pairs = run_duplication(repo)

    return {
        "name": repo_name,
        "cpp_files": cpp_count,
        "cpp_loc": cpp_loc,
        "commits": commit_count,
        "ai_commits": ai_count,
        "ai_pct": f"{ai_pct:.1f}",
        "graph_errors": graph_errors,
        "dup_pairs": dup_pairs,
        "about": about,
    }


def load_repos(tsv_path: Path, limit: int = 0) -> list[str]:
    """Load repo names from all_local.tsv."""
    repos = []
    try:
        with open(tsv_path, "r", encoding="utf-8") as f:
            reader = csv.reader(f, delimiter="\t")
            for row in reader:
                if len(row) >= 2:
                    repos.append(row[1])
                    if limit > 0 and len(repos) >= limit:
                        break
    except (OSError, IOError) as e:
        print(f"error reading {tsv_path}: {e}", file=sys.stderr)
    return repos


def main() -> int:
    args = parse_args()

    # Load repos
    repos = load_repos(ALL_LOCAL_TSV, args.limit)
    print(f"loaded {len(repos)} repos from {ALL_LOCAL_TSV}", file=sys.stderr)

    # Scan in parallel
    results = []
    skipped = 0

    with ProcessPoolExecutor(max_workers=args.parallel) as executor:
        futures = {
            executor.submit(scan_repo, name, args.since, args.cpp_min, args.skip_graph, args.skip_dup): name
            for name in repos
        }

        done = 0
        for future in as_completed(futures):
            done += 1
            repo_name = futures[future]
            try:
                record = future.result()
                if record:
                    results.append(record)
                else:
                    skipped += 1
                if done % 50 == 0:
                    print(f"  {done}/{len(repos)}: {done - skipped} included, {skipped} skipped", file=sys.stderr)
            except Exception as e:
                print(f"error scanning {repo_name}: {e}", file=sys.stderr)
                skipped += 1

    print(f"✓ scanned {len(repos)} repos: {len(results)} included, {skipped} skipped", file=sys.stderr)

    # Sort by ai_pct descending
    results.sort(key=lambda r: float(r["ai_pct"]), reverse=True)

    # Write TSV summary
    summary_path = ORCHESTRATOR_DIR / "corpus_summary.tsv"
    with open(summary_path, "w", encoding="utf-8") as f:
        headers = ["name", "cpp_files", "cpp_loc", "commits", "ai_commits", "ai_pct", "graph_errors", "dup_pairs", "about"]
        f.write("\t".join(headers) + "\n")
        for record in results:
            values = [str(record.get(h, "")) for h in headers]
            f.write("\t".join(values) + "\n")

    print(f"→ {summary_path}", file=sys.stderr)

    # Write markdown report
    report_path = ORCHESTRATOR_DIR / "corpus_report.md"
    with open(report_path, "w", encoding="utf-8") as f:
        f.write("# Corpus Report\n\n")
        f.write(f"**Scanned:** {len(results)} C++ repos\n")
        f.write(f"**Window:** {args.since} → {datetime.now().date()}\n")
        f.write(f"**Generated:** {datetime.now().isoformat()}\n\n")

        f.write("## By AI commit percentage\n\n")
        f.write("| Rank | Repo | AI% | Commits | Graph Errors | Dup Pairs | LOC |\n")
        f.write("|------|------|-----|---------|--------------|-----------|-----|\n")
        for i, r in enumerate(results[:50], 1):  # Top 50
            f.write(f"| {i} | {r['name']} | {r['ai_pct']}% | {r['commits']} | {r['graph_errors']} | {r['dup_pairs']} | {r['cpp_loc']} |\n")

        f.write(f"\n... and {len(results) - 50} more repos (see corpus_summary.tsv)\n")

    print(f"→ {report_path}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
